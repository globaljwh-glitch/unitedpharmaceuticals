                        <h4>ANTITRUST</h4>
                        <h6>Policy Overview:</h6>
                        <p>We believe that open competition in a free marketplace will lead to appropriate prices and
                            promote an efficient and productive economy. Antitrust laws prohibit agreements that
                            unreasonably restrain trade and anticompetitive behavior that result in monopolies. These laws
                            apply to conduct that has economic effects regardless of where it occurs.</p>
                        <ul class="listing01">
                            <li>We are committed to fair sales practices.</li>
                            <li>We will not engage in practices that would unfairly restrain trade or exclude competitors
                                from the marketplace.</li>
                            <li>We will not communicate or agree, formally or informally, with competitors to fix or
                                stabilize prices, limit production, allocate markets or customers, or boycott customers or
                                suppliers.</li>
                        </ul>
                        <p class="highlightedText">Key Requirements:</p>
                        <p>Failure to comply with antitrust laws can be extremely damaging to our company and our employees.
                            You must bring business dealings that raise questions or concerns about antitrust to the
                            attention of Legal Services.</p>
                        <p class="highlightedText"><em>Generally, the antitrust laws prohibit the following conduct</em></p>
                        <ul class="listing01">
                            <li>Price discrimination: selling the same product at different prices to different customers
                                when that price difference might have an adverse effect on competition. In general, we
                                should treat all similarly situated customers the same. This rule is subject to numerous
                                exceptions and qualifications. Therefore, you must work closely with management to approve
                                our pricing practices and review any deviation from established procedures.</li>
                            <li>Price fixing: agreements and understandings between competitors to raise, lower, maintain,
                                stabilize, or otherwise fix prices. Competitors may not agree on the prices they will charge
                                for their products. No employees shall ever discuss pricing practices with a competitor
                                except where that competitor is a customer and the discussion is in conjunction with a
                                specific sales transaction in the normal course of business.</li>
                            <li>Minimum resale price maintenance: dictating to our distributors the minimum prices at which
                                they should sell our products. Such “vertical” price fixing is per se illegal.</li>
                            <li>Bid rigging: agreements or understandings between competitors to rig bids or proposals, such
                                as by (1) agreeing on prices or other terms and conditions; (2) agreeing to rotate or
                                alternate submission of bids; (3) agreeing that one competitor will bid for certain
                                contracts or customers, while other competitors will bid for different contracts or
                                customers.</li>
                            <li>Market division: agreements or understandings by which competitors divide the market in
                                which they compete, such as by allocating customers, territories, or products among
                                themselves.</li>
                            <li>Boycotts or concerted refusals to deal: agreements or understandings by which two or more
                                companies jointly refuse to do business with other companies for the purpose of eliminating
                                competition. As a company, we can decide with whom we wish to do business. But we cannot
                                exercise this right in conjunction with other companies. That would be a boycott—which is
                                illegal.</li>
                            <li>Tying arrangements: transactions in which a customer’s purchase of a product that it wants
                                is conditioned on purchase of another product that the customer does not want, where the
                                seller has a very strong market position (“market power”) with respect to the product the
                                customer does not want. Each of our products and services should be able to stand in the
                                marketplace on its own.</li>
                            <li>Monopolization: attempting to achieve, achieving, or maintaining “monopoly power,” i.e., the
                                ability to control prices or exclude competition through illegal or unfair exclusionary
                                practices in a particular market. While we believe that our company does not have—nor is it
                                likely to have—a monopolistic position in any relevant market, we cannot be sure that a
                                court of law would not define a relevant market so narrowly as to raise the question about
                                monopolization.</li>
                        </ul>
                        <h4>Do’s and Don’ts:</h4>
