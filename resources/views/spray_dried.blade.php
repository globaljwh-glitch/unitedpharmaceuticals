                    <h2>HOW SPRAY DRYING WORKS: TURNING SCIENCE INTO SCALABLE SOLUTIONS</h2>
                    <p>Spray drying is a proven particle engineering technology that transforms liquid formulations into dry
                        powders in a single, continuous step. By rapidly drying atomized droplets in a controlled
                        environment, this process creates uniform, stable particles—ideal for improving solubility,
                        bioavailability, and manufacturability of challenging drug compounds.</p>
                    <p><b>Dissolution:</b> The drug and polymer are dissolved in a suitable solvent, often a volatile
                        organic solvent like ethanol, acetone, or tetrahydrofuran. The choice of solvent is an important
                        consideration; our manufacturing experts will select the most appropriate solvent based on the
                        specifics of your program.</p>
                    <p><b>Atomization:</b> The solution is then atomized into a fine mist of droplets using either a rotary
                        atomizer or a nozzle atomizer. This breaks the liquid into tiny droplets, increasing the surface
                        area for efficient drying.</p>
                    <p><b>Drying:</b> The fine droplets go into a drying chamber where they are dried with hot air ranging
                        in temperature from 100°C to 200°C, depending on the thermal sensitivity of the API. The hot air
                        rapidly evaporates the solvent, leaving behind solid particles of drug product.</p>
                    <p><b>Collection:</b> The dried particles, which are now an amorphous solid dispersion, are collected
                        using a cyclone separator or a bag filter. These particles are further processed or formulated into
                        the final product.</p>
